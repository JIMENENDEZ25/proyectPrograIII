    // ========== INICIALIZACIÓN ==========
    private void inicializarPersonalizado() {
        // 1. Crear el controller
        this.controller = new AtencionController(tipoAgente, nombreAgente);

        // 2. Configurar la tabla de clientes en espera
        String[] columnas = {"Ticket", "DPI", "Nombre", "Hora Ingreso"};
        modeloTablaEspera = new DefaultTableModel(columnas, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        jTable1.setModel(modeloTablaEspera);
        jTable1.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        // 3. Configurar título según el tipo
        if (tipoAgente.equals("premium")) {
            titulo.setText("⭐ ATENCIÓN PREMIUM");
            titulo.setForeground(new Color(255, 153, 102));
            btn_takeNextCliente.setBackground(new Color(255, 153, 102));
            titulo2.setText("Clientes en Espera (PREMIUM)");
        } else {
            titulo.setText("📞 ATENCIÓN GENERAL");
            titulo.setForeground(new Color(102, 102, 255));
            btn_takeNextCliente.setBackground(new Color(102, 102, 255));
            titulo2.setText("Clientes en Espera (NORMAL)");
        }

        // 4. Configurar áreas de texto
        jTextPaneActual.setEditable(false);
        jTextPaneActual1.setEditable(false);
        jTextPaneActual1.setFont(new Font("Monospaced", Font.PLAIN, 11));

        // 5. Configurar campos de formulario (inicialmente deshabilitados)
        txt_motive.setEnabled(false);
        txt_duration.setEnabled(false);
        btn_finalizarAtencion.setEnabled(false);

        // 6. Configurar texto del agente
        agent_txt.setText(nombreAgente);

        // 7. Configurar IP por defecto
        txt_ip.setText(controller.getHostActual());

        // 8. Configurar eventos
        configurarEventos();

        // 9. Configurar callbacks del controller
        configurarCallbacks();

        // 10. Conectar al servidor
        controller.conectar();
    }

    private void configurarEventos() {
        // Botón Aplicar IP
        btn_apply_IP.addActionListener(e -> {
            String nuevaIp = txt_ip.getText().trim();
            if (!nuevaIp.isEmpty()) {
                controller.cambiarIpServidor(nuevaIp);
            }
        });

        // Botón IP Predefinida
        btn_preset_IP.addActionListener(e -> {
            String ipPredefinida = "25.4.40.151"; // Cambia según tu configuración
            txt_ip.setText(ipPredefinida);
            controller.cambiarIpServidor(ipPredefinida);
            agregarLog("🔧 Usando IP predefinida: " + ipPredefinida);
        });

        // Botón Tomar Siguiente Cliente
        btn_takeNextCliente.addActionListener(e -> controller.tomarSiguienteCliente());

        // Botón Finalizar Atención
        btn_finalizarAtencion.addActionListener(this::finalizarAtencion);

        // Botón Chat
        btn_chat.addActionListener(e -> abrirChat());

        // Doble click en tabla para tomar cliente específico
        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                if (evt.getClickCount() == 2) {
                    int row = jTable1.getSelectedRow();
                    if (row >= 0) {
                        if (controller.getClienteActual() != null) {
                            JOptionPane.showMessageDialog(null,
                                    "Ya tienes un cliente en atención. Finalízalo primero.",
                                    "Cliente activo",
                                    JOptionPane.WARNING_MESSAGE);
                            return;
                        }

                        String ticket = modeloTablaEspera.getValueAt(row, 0).toString();
                        String nombre = modeloTablaEspera.getValueAt(row, 2).toString();
                        String hora = modeloTablaEspera.getValueAt(row, 3).toString();

                        int confirm = JOptionPane.showConfirmDialog(
                                null,
                                "¿Tomar este cliente?\n\nTicket: " + ticket + "\nCliente: " + nombre + "\nIngreso: " + hora,
                                "Confirmar atención",
                                JOptionPane.YES_NO_OPTION,
                                JOptionPane.QUESTION_MESSAGE
                        );

                        if (confirm == JOptionPane.YES_OPTION) {
                            controller.tomarClienteSeleccionado(ticket);
                        }
                    }
                }
            }
        });
    }

    private void configurarCallbacks() {
        controller.setOnConectar(() -> {
            server_Status.setText("🟢 Conectado al Servidor");
            server_Status.setForeground(new Color(0, 150, 0));
            btn_takeNextCliente.setEnabled(true);
            txt_ip.setText(controller.getHostActual());
        });

        controller.setOnErrorConexion(() -> {
            server_Status.setText("🔴 Sin conexión");
            server_Status.setForeground(Color.RED);
            btn_takeNextCliente.setEnabled(false);
        });
        
        controller.setOnAtencionRegistrada(() -> {
            SwingUtilities.invokeLater(() -> {
                txt_motive.setText("");
                txt_duration.setText("");
                habilitarRegistroAtencion(false);
                agregarLog("✅ Atención registrada exitosamente");
                controller.actualizarListaClientes();
                btn_takeNextCliente.setEnabled(true);
            });
        });
        controller.setOnLog(this::agregarLog);

        controller.setOnActualizarContadores((normal, premium) -> {
            normal_counter.setText(String.valueOf(normal));
            premium_counter.setText(String.valueOf(premium));
        });

        controller.setOnClienteAsignado(respuestaJson -> {
            String ticket = respuestaJson.get("ticket").getAsString();
            String dpi = respuestaJson.get("dpi").getAsString();
            String nombre = respuestaJson.get("nombre").getAsString();
            String apellido = respuestaJson.get("apellido").getAsString();
            String tipo = respuestaJson.get("tipo").getAsString();

            Cliente nuevoCliente = new Cliente(ticket, dpi, nombre, apellido, tipo);
            mostrarClienteEnUI(nuevoCliente);
            controller.setClienteActual(nuevoCliente);
            habilitarRegistroAtencion(true);
            agregarLog("📞 Cliente asignado: " + ticket + " - " + nombre + " " + apellido);
            controller.actualizarListaClientes();
        });

        controller.setOnError(errorMsg -> agregarLog("❌ Error: " + errorMsg));

        controller.setOnClienteFinalizado(() -> {
            jTextPaneActual.setText("✅ Atención finalizada\n\nPresione 'Tomar Siguiente Cliente' para continuar.");
            txt_motive.setText("");
            txt_duration.setText("");
            habilitarRegistroAtencion(false);
            controller.setClienteActual(null);
        });

        controller.setOnActualizarTabla(this::actualizarTablaClientes);

        // Callbacks para chat
        controller.setOnMensajeChat(mensaje -> {
            SwingUtilities.invokeLater(() -> {
                if (txtAreaChat != null) {
                    txtAreaChat.append(mensaje + "\n");
                    txtAreaChat.setCaretPosition(txtAreaChat.getDocument().getLength());
                }
            });
        });

        controller.setOnActualizarUsuariosChat(usuarios -> {
            SwingUtilities.invokeLater(() -> {
                if (modeloUsuarios != null) {
                    modeloUsuarios.clear();
                    for (var u : usuarios) {
                        modeloUsuarios.addElement(u.getAsString());
                    }
                }
            });
        });
    }

