    private void actualizarTablaClientes(java.util.List<JsonObject> clientes) {
        modeloTablaEspera.setRowCount(0);
        for (JsonObject obj : clientes) {
            modeloTablaEspera.addRow(new Object[]{
                obj.get("ticket").getAsString(),
                obj.get("dpi").getAsString(),
                obj.get("nombre").getAsString(),
                obj.get("hora").getAsString()
            });
        }
    }

    private void finalizarAtencion(ActionEvent evt) {
        String motivo = txt_motive.getText().trim();
        String duracionStr = txt_duration.getText().trim();

        if (motivo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el motivo de la atención");
            return;
        }

        int duracion;
        try {
            duracion = Integer.parseInt(duracionStr);
            if (duracion < 1 || duracion > 480) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ingrese una duración válida (1-480 minutos)");
            return;
        }

        Cliente clienteActual = controller.getClienteActual();
        if (clienteActual == null) {
            agregarLog("❌ No hay cliente actual para finalizar");
            return;
        }

        // Confirmar finalización
        int confirm = JOptionPane.showConfirmDialog(
                null,
                "¿Finalizar atención?\n\nTicket: " + clienteActual.getTicket() + "\nCliente: " + clienteActual.getNombreCompleto()
                + "\n\nMotivo: " + motivo + "\nDuración: " + duracion + " minutos",
                "Confirmar finalización",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        btn_finalizarAtencion.setEnabled(false);
        btn_takeNextCliente.setEnabled(false);

        controller.finalizarAtencion(motivo, duracion);
        agregarLog("⏳ Finalizando atención...");
    }

    private void habilitarRegistroAtencion(boolean habilitado) {
        txt_motive.setEnabled(habilitado);
        txt_duration.setEnabled(habilitado);
        btn_finalizarAtencion.setEnabled(habilitado);
        if (habilitado) {
            txt_motive.requestFocus();
        }
    }

    private void agregarLog(String mensaje) {
        SwingUtilities.invokeLater(() -> {
            String timestamp = java.time.LocalDateTime.now()
                    .format(java.time.format.DateTimeFormatter.ofPattern("HH:mm:ss"));
            String linea = "[" + timestamp + "] " + mensaje + "\n";

            // Usar jTextPaneActual1 como área de log (Actividades)
            String actual = jTextPaneActual1.getText();
            jTextPaneActual1.setText(actual + linea);

            // Auto-scroll al final
            jTextPaneActual1.setCaretPosition(jTextPaneActual1.getDocument().getLength());

            // Limitar líneas (opcional)
            if (jTextPaneActual1.getText().split("\n").length > 200) {
                String[] lineas = jTextPaneActual1.getText().split("\n");
                StringBuilder sb = new StringBuilder();
                for (int i = lineas.length - 150; i < lineas.length; i++) {
                    if (i >= 0) {
                        sb.append(lineas[i]).append("\n");
                    }
                }
                jTextPaneActual1.setText(sb.toString());
            }
        });
    }

